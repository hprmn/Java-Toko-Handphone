    public void simpanKonfigurasi(){
        konfigurasi konf = new konfigurasi();
        konf.edit("DBHost", host.getText());
        konf.edit("DBPort", port.getText());
        konf.edit("DBDatabase", database.getText());
        konf.edit("DBUsername", username.getText());
        konf.edit("DBPassword", password.getText());
        getdataKoneksi();
    }
