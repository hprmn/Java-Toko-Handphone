    private void btn_konfigurasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_konfigurasiActionPerformed
        // TODO add your handling code here:
        if(btn_konfigurasi.getText().equalsIgnoreCase("EDIT")){
            editKonfigurasi(true);
            host.requestFocus();
            btn_konfigurasi.setText("SIMPAN");
        }else if (btn_konfigurasi.getText().equalsIgnoreCase("SIMPAN")){
            simpanKonfigurasi();
            editKonfigurasi(false);
            JOptionPane.showMessageDialog(null, "Konfigurasi Berhasil Disimpan");
            btn_konfigurasi.setText("EDIT");
            
        }
    }//GEN-LAST:event_btn_konfigurasiActionPerformed

